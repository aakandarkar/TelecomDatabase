CREATE TABLE account
(
	acct_id	INT NOT NULL,
    acct_fname VARCHAR(20) NOT NULL,
    acct_lname	VARCHAR(20) NOT NULL,
    acct_eff_dt	DATE NOT NULL,
    acct_end_dt	DATE NOT NULL,
    acct_cust_sgmnt_cd CHAR(1) NOT NULL CHECK(acct_cust_sgmnt_cd IN('H','O','E','I')),
    load_dt_tm	DATETIME NOT NULL,
    updt_dt_tm	DATETIME NOT NULL,
	CONSTRAINT account_PK PRIMARY KEY(acct_id)
)
;

CREATE TABLE product
(
	prd_id INT NOT NULL,
	prd_desc VARCHAR(50) NOT NULL,		
	prd_eff_dt DATE NOT NULL,
	prd_end_dt DATE NOT NULL,	
	prd_price DECIMAL(5,2) NOT NULL,
	load_dt_tm	DATETIME NOT NULL,
    updt_dt_tm	DATETIME NOT NULL,
	CONSTRAINT product_PK PRIMARY KEY(prd_id)
)
;

CREATE TABLE campaign
(
	camp_id INT NOT NULL,
	camp_desc VARCHAR(50) NOT NULL,
	camp_eff_dt DATE NOT NULL,
	camp_end_dt DATE NOT NULL,
	load_dt_tm	DATETIME NOT NULL,
    updt_dt_tm	DATETIME NOT NULL,
	CONSTRAINT campaign_PK PRIMARY KEY(camp_id)
)
;

CREATE TABLE billing
(
	billing_id INT NOT NULL,
	acct_id	INT NOT NULL,
	billing_address VARCHAR(50) NOT NULL,
	billing_payment_mode VARCHAR(2) NOT NULL CHECK(billing_payment_mode IN('C','D','CA')),
	billing amount DECIMAL(5,2) NOT NULL,
	billing_eff_dt DATE NOT NULL,
	billing_end_dt DATE NOT NULL,
    load_dt_tm	DATETIME NOT NULL,
    updt_dt_tm	DATETIME NOT NULL,
	CONSTRAINT billing_PK PRIMARY KEY(billing_id),
	CONSTRAINT billing_FK FOREIGN KEY(acct_id) REFERENCES account(acct_id)
)
;	

CREATE TABLE customer_location
(
	cust_loc_id INT NOT NULL,
	acct_id	INT NOT NULL UNIQUE,
	srv_addr VARCHAR(50) NOT NULL,
	cust_city VARCHAR(20) NOT NULL,
	cust_state VARCHAR(2) NOT NULL,
	cust_zipcode INT NOT NULL,
	cust_loc_eff_dt DATE NOT NULL,
	cust_loc_end_dt DATE NOT NULL,
	load_dt_tm	DATETIME NOT NULL,
    updt_dt_tm	DATETIME NOT NULL,
	CONSTRAINT cloc_PK PRIMARY KEY(cust_loc_id),
	CONSTRAINT cloc_FK FOREIGN KEY(acct_id) REFERENCES account(acct_id)
)
;

CREATE TABLE subscription
(
	subscr_id INT NOT NULL,
	acct_id	INT NOT NULL,
	prd_id INT NOT NULL,
	srv_cd VARCHAR(4) NOT NULL CHECK(srv_cd IN('M','T','IW','IWL','CT','OTT','VOIP')),
	srv_desc VARCHAR(50) NOT NULL,
	srv_eff_dt DATE NOT NULL,
	srv_end_dt DATE NOT NULL,
	srv_addr VARCHAR(50) NOT NULL,
	load_dt_tm	DATETIME NOT NULL,
    updt_dt_tm	DATETIME NOT NULL,
	CONSTRAINT subscription_PK PRIMARY KEY(subscr_id),
	CONSTRAINT subscription_FK_acct FOREIGN KEY(acct_id) REFERENCES account(acct_id),
	CONSTRAINT subscription_FK_prd FOREIGN KEY(prd_id) REFERENCES product(prd_id),
	CONSTRAINT unique_subscription UNIQUE(acct_id,prd_id)
)
;

CREATE TABLE offer
(
	offr_cd VARCHAR(10) NOT NULL,
	prd_id	INT NOT NULL UNIQUE,
	offr_desc VARCHAR(50) NOT NULL,
	offr_eff_dt DATE NOT NULL,
	offr_end_dt DATE NOT NULL,
	load_dt_tm	DATETIME NOT NULL,
    updt_dt_tm	DATETIME NOT NULL,
	CONSTRAINT offer_PK PRIMARY KEY(offr_cd),
	CONSTRAINT offer_FK FOREIGN KEY(prd_id) REFERENCES product(prd_id)
)
;	

CREATE TABLE ordert
(
	order_id INT NOT NULL,
	acct_id	INT NOT NULL,
	prd_id INT NOT NULL,
	order_desc VARCHAR(50) NOT NULL,
	order_eff_dt DATE NOT NULL,
	order_end_dt DATE NOT NULL,
	load_dt_tm	DATETIME NOT NULL,
    updt_dt_tm	DATETIME NOT NULL,
	CONSTRAINT order_PK PRIMARY KEY(order_id),
	CONSTRAINT order_FK_acct FOREIGN KEY(acct_id) REFERENCES account(acct_id),
	CONSTRAINT order_FK_prd FOREIGN KEY(prd_id) REFERENCES product(prd_id),
	CONSTRAINT unique_order UNIQUE(acct_id,prd_id)
)
;

CREATE TABLE productcampaign
(
	prod_camp_id INT NOT NULL,
	prd_id INT,
	camp_id INT,
	CONSTRAINT productcampaign_PK PRIMARY KEY(prod_camp_id),
	CONSTRAINT productcampaign_FK_campaign FOREIGN KEY(camp_id) REFERENCES campaign(camp_id),
	CONSTRAINT productcampaign_FK_prd FOREIGN KEY(prd_id) REFERENCES product(prd_id)
)
;

